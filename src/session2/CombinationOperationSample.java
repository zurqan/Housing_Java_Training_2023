package session2;

public class CombinationOperationSample {


    public static void main(String[] args) {

        int number =10;
        System.out.println("number = " + number);
        number += 15;
        System.out.println("number = " + number);

        number -=5;
        System.out.println("number = " + number);

        number *=5;
        System.out.println("number = " + number);


        System.out.println("(100 == number) = " + (100 == number));
        System.out.println("(100 != number) = " + (100 != number));


    }
}
